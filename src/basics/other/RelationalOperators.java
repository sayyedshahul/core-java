package basics.other;

public class RelationalOperators {
    public static void main(String[] args){
        int n1 = 2;
        int n2 = 5;
        boolean result;

        result = n1 > n2;
        System.out.println(result);
        result = n1 < n2;
        System.out.println(result);
        result = n1 == n2;
        System.out.println(result);
        result = n1 >= n2;
        System.out.println(result);
        result = n1 <= n2;
        System.out.println(result);
        result = n1 != n2;
        System.out.println(result);
    }
}
