package basics;

public class DataTypes {
    public static void main(String[] args){
        // Integer subtypes
        int num1 = 4;
        short sh = 31234;
        byte by =  125;
        long lng = 8368288L;

        //Float subtypes
        float temperature = 43.76f;
        double temp = 32.987;

        //For single char values.
        char c = 'A';

        //For true or false values.
        boolean condition = true;
    }
}
